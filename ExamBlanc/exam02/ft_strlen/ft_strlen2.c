#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
		while (*str)
		{
			i++;
			str++;
		}
		return (i);
}

int	main()
{
	printf("size of Word  is %d", ft_strlen("  "));
}
