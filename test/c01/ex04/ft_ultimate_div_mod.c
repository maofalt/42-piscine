void	ft_ultimate_div_mod(int *a, int *b)
{
	int	temp_a;
	int	temp_b;

	temp_a = *a;
	temp_b = *b;
	if (b != 0)
	{
		*a = temp_a / temp_b;
		*b = temp_a % temp_b;
	}
}
