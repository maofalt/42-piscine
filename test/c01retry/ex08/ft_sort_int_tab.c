void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	aux;

	i = 0;
	j = 0;
	while (i < size - 1)
	{
		j = i;
		while (j < size - 1)
		{
			if (tab[i] > tab [j + 1])
			{
				aux = tab[i];
				tab[i] = tab[j + 1];
				tab[j + 1] = aux;
			}
			j++;
		}
		i++;
	}
}
