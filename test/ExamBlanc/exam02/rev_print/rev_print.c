#include <unistd.h>
#include <stdio.h>
int main(int argc, char **argv)
{
	
	int	i;

	i = 0;
	(void)argc;	
	while (argv[1][i])
		i++;
	while (--i >= 0)
		write(1, &argv[1][i], 1);
	write(1,"\n",1);
}
